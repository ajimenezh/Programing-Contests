using namespace std;
int dp[105][105];
int n;

int possible(int i, int rest) {
	if (rest==0 && i==n) return true;
	if ((i==n && rest!=0) || i>n) return false;
	if (dp[i][rest]!=-1) return dp[i][rest];
	
	bool exist=false;
	
	exist = exist || possible(i+1, rest-1);
	exist = exist || possible(i+1, rest + i);
	
	dp[i][rest]=exist;
	
	return exist;
}
	

class IdealString {
public:
	string construct(int);
};

string IdealString::construct(int length) {
	n = length;
	string s;
	vector<char> letters;
	queue<char> remain;
	for (char i='Z'; i>'A'; i--) letters.push_back(i);
	for (int i=0; i<n; i++) for (int j=0; j<n; j++) dp[i][j]=-1;
	
	s=s+'A';
	if (n==1) return s;
	
	int k=1;
	while (true) {
		if (possible(k+1,remain.size()+k)) {
			s = s + letters.back();
			letters.pop_back();
			for (int i=0; i<k; i++) remain.push(s[s.length()-1]);
			k++;
		}
		else if (remain.size()!=0) {
			s+=remain.front();
			remain.pop();
			k++;
		}
		else break;
		//cout<<s<<" "<<remain.size()<<endl;
		if (s.length()>n || (s.length() + remain.size())>n) break;
		if (s.length()==n && remain.size()==0) return s;
	}
	
	return "";
	
}


