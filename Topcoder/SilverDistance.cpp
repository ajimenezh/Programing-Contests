class SilverDistance {
public:
	int minSteps(int, int, int, int);
};

int SilverDistance::minSteps(int sx, int sy, int gx, int gy) {
	int x = gx - sx;
	int y = gy - sy;
	if (x > 0  && x >=y) return x;
	if (x>0 && abs(x)>=y) {
		if (abs(x)%2==0) return abs(x);
		return abs(x +1);
	}
	return abs(y) + abs(y) + x;
}


