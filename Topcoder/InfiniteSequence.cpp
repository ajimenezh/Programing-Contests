class InfiniteSequence {
public:
	long long calc(long long, int, int);
};

map<int,long long> m;
int p,q;
long long rec(int a){
	if (a==0) return 1;
	if (m.find(a)!=m.end()) return m[a];
	
	long long b = rec(a/p) + rec(a/q);
	
	m[a]=b;
	
	return b;
}
long long InfiniteSequence::calc(long long n, int _p, int _q) {
	p = _p;
	q = _q;
	if (n==0) return 1;
	return rec(n/q) + rec(n/p);
}


