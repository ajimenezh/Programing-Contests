class TheCowDivTwo {
public:
	int find(int, int);
};


int TheCowDivTwo::find(int N, int K) {
	int dp[2][48][1001];
	long long mod = 1000000007;
	
	for (int p=N; p>-1; p--) {
		for (int i=0; i<=K; i++) {
			for (int j=0; j<N; j++) {
				if (i==0) {
					if (j==0) dp[p%2][i][j]=1;
					if (j!=0) dp[p%2][i][j]=0;
				}
				else if (p==N) dp[p%2][i][j]=0;
				else {
					dp[p%2][i][j] = (dp[(p+1)%2][i-1][(j-p+N)%N] + dp[(p+1)%2][i][j])%mod;
				}
			}
		}
	}
	
	return dp[0][K][0];
	
				
}


