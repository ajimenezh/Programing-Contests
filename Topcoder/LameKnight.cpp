class LameKnight {
public:
	int maxCells(int, int);
};

int LameKnight::maxCells(int height, int width) {
	if (height == 1) return 1;
	if (height <4) {
		if (width<3) return 1;
		if (width<5) return 2;
		return 3;
	}
	
	if (width <= 4) return width;
	if (width<7) return width - 1;
	
	return width - 2;
}


