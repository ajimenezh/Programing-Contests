class CubePacking {
public:
	int getMinimumVolume(int, int, int);
};

int CubePacking::getMinimumVolume(int Ns, int Nb, int L) {
	int a = Ns  + Nb*L*L*L;
	
	for (int i=L; i*i*i<=a; i++) for (int j=i; j*i*i<=a; j++) {
		int b = i*j;
		if ((a/b)*b == a) {
			int c = a/b;
			if ((i/L)*(j/L)*(c/L)>=Nb) {
				cout<<i<<" "<<j<<" "<<c<<endl;
				return a;
			}
		}
	}
	return Nb*L*L*L + (Ns/(L*L))*L*L + L*L;
}


