class SolitaireSimulation {
public:
	int periodLength(vector <int>);
};

int SolitaireSimulation::periodLength(vector <int> heaps) {
	map<vector<int>, int> m;
	vector<int> v1 = heaps;
	int k=0;
	int sum = 0;
	for (int i=0; i<v1.size(); i++) sum+=v1[i];
	for (int i=0; i<=(sum*sum); i++) {
		vector<int> v2;
		sort(v1.begin(),v1.end());
		if (m.find(v1)!=m.end()) {
			return k - m[v1];
		}
		m[v1] = k++;
		
		
		for (int j=0; j<v1.size(); j++) if (v1[j]>1) v2.push_back(v1[j] -1);
		v2.push_back(v1.size());
		v1 = v2;
	}
	
	return 0;
}


