class DoubleXor {
public:
	int calculate(int);
};

int DoubleXor::calculate(int N) {
	int n = 0;
	for (int i=N; i>0; i--) n = (i | n);
	
	return n;
}


double test0() {
	int p0 = 1;
	DoubleXor * obj = new DoubleXor();
	clock_t start = clock();
	int my_answer = obj->calculate(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 1;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
