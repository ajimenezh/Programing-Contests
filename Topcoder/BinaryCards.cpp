class BinaryCards {
public:
	long long largestNumber(long long, long long);
};


long long BinaryCards::largestNumber(long long A, long long B) {
	string a,b;
	for (int i=0; i<64; i++) {

		if ((A & (long long)(1LL<<i))!=0) a = '1' + a;
		else a = '0' + a;
		if ((B & (long long)(1LL<<i))!=0) b = '1' + b;
		else b = '0' + b;

	}
	
	cout<<a<<" "<<b<<" "<<b.length()<<endl;
	
	long long t=0;
	for (int i=0; i<b.length(); i++) {
		if (b[i]==a[i] && b[i]=='1') t += 1LL<<(63-i);
		else if (b[i]!=a[i]) {
			t += (1LL<<(64-i)) - 1;
			break;
		}
	}
	
	 return t; 
}


