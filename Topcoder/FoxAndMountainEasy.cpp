class FoxAndMountainEasy {
public:
	string possible(int, int, int, string);
};

string FoxAndMountainEasy::possible(int n, int h0, int hn, string history) {
	int t0,tf;
	t0 = 0;
	tf = 0;
	int mi = 1000000;
	for (int i=0; i<history.length(); i++) {
		if (history[i]=='U') tf++;
		else tf--;
		mi = min(mi,tf);
	}
	
	int s = history.length();
	
	for (int i=0; i<=300000; i++) {
		if ((i+mi)<0) continue;
		int s1 = abs(i-h0);
		s1 += s;
		s1 = n - s1;
		if (s1 >= abs(hn-(i+tf))) {
			cout<<i<<endl;
			if ((s1 -abs(hn-(i+tf)))%2==0) return "YES";
		}
	}
	
	return "NO";
}


