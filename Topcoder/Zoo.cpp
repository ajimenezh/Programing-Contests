class Zoo {
public:
	long long theCount(vector <int>);
};

long long Zoo::theCount(vector <int> answers) {
	int a[41];
	for (int i=0; i<41; i++) a[i]=0;
	for (int i=0; i<answers.size(); i++) a[answers[i]]++;
	
	for (int i=0; i<40; i++) if (a[i]>2 || (i>0 && a[i]>a[i-1])) return 0;
	
	int count1=0; int count2=0;
	
	for (int i=0; i<40; i++) {
		if (a[i]==2) count1++;
		if (a[i]==1) count2++;
	}
	
	if (count2>0) count1++;
	return 1<<count1;
	
}


