int n;
double pow(int a, int b) {
	if (b==0) return 1;
	if (b%2==0) return pow(a*a,b/2);
	else return a*pow(a,b-1);
}
double f(string s, double base) {
	double a=0.0;
	for (int i=n-1; i>=0; i--) {
		if (s[i]=='1') a += pow(base,n-i-1);
	}
	return a;
}
double AgeEncoding::getRadix(int age, string candlesLine) {
	n = candlesLine.length();
	if (candlesLine=="1" && age == 1) return -2;
	double lo = 0.0;
	double hi = 105.0;
	double mi;
	
	for (int i=0; i<100; i++) {
		mi = lo + (hi-lo)/2.0;
		
		double num = f(candlesLine,mi);
		if (num>age) hi =mi;
		else lo = mi;
	}
	
	if (mi<101) return mi;
	else return -1;
}


